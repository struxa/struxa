<?php

declare(strict_types=1);

/**
 * Renders the live homepage (GET /) through the full Slim stack and writes HTML to disk.
 * Single source of truth remains Twig; this output is an optional snapshot for demos or static mirrors.
 */

use Slim\Psr7\Factory\ServerRequestFactory;

$root = dirname(__DIR__);

$options = getopt('', ['output::']);
$outPath = isset($options['output']) && is_string($options['output']) && $options['output'] !== ''
    ? $options['output']
    : $root . '/public/marketing-preview.html';

$app = require $root . '/bootstrap/web_app.php';

$request = (new ServerRequestFactory())->createServerRequest('GET', '/');
$response = $app->handle($request);

$status = $response->getStatusCode();
if ($status !== 200) {
    fwrite(STDERR, "Homepage render returned HTTP {$status}; not writing file.\n");
    exit(1);
}

$body = (string) $response->getBody();
$banner = "<!-- Generated by bin/render-marketing-static.php — do not hand-edit. Regenerate after Twig/CSS/JS changes. -->\n";
file_put_contents($outPath, $banner . $body);

fwrite(STDOUT, "Wrote {$outPath}\n");
